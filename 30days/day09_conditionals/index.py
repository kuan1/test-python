# If Condition
if 1 > -1:
  print(True)

# If Else
if 1 > 2:
  print('error')
else:
  print('1 < 2')

# If Elif Else
if 1 > 2:
  print('error')
elif 2 == 2:
  print('2 == 2')
else:
  print('error')

# If Condition and Logical Operators
if 1 < 2 and 3 < 4:
  print('1 < 2 and 3 < 4')

# If and Or Logical Operators
if 0 > 3 or 1 > 0:
  print('0 > 3 or 1 > 0')
