a = {1, 2, 3, 4, 5}
b = {3, 4, 5, 6, 7}

# 交集运算 &
print(a & b)

# 并集 |
print(a | b)

# 差集 -
print(a - b)

# 异或集 ^
print(a ^ b)

# 检查是不是子集 <=
a = {2, 3, 4}
b = {1, 2, 3, 4}
print(a <= b)

# 检查是不是真子集
a = {1, 2}
b = {1, 2, 3}
print(a < b)


# 检查是不是超集 >=
a = {1, 2, 3, 4}
b = {1, 2, 3, 4}
print(a >= b)

# 检查是不是真子集
a = {1, 2, 3}
b = {1, 2}
print(a > b)
